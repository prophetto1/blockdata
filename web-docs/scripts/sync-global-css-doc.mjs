import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const cssPath = resolve(process.cwd(), 'src/styles/global.css');
const outputPath = resolve(
  process.cwd(),
  'src/content/docs/internal/style-guide/current-configs/global-css.md',
);

const source = readFileSync(cssPath, 'utf8');
const escapedSource = source.replaceAll('```', '`\\u200b``');
const now = new Date().toISOString();

const lines = [
  '---',
  'title: Global CSS',
  'description: Canonical, human-readable snapshot of the live docs stylesheet used by the site.',
  '---',
  '',
  '<!--',
  'DO NOT EDIT THIS FILE DIRECTLY.',
  `Generated from src/styles/global.css on ${now}.`,
  'Run: npm run sync:global-css-doc',
  '-->',
  '',
  'This page mirrors <code>src/styles/global.css</code> for reference from the doc site file tree.',
  '',
  '```css',
  escapedSource,
  '```',
].join('\n');

const doc = `${lines}\n`;

writeFileSync(outputPath, doc);
console.log(`Wrote ${outputPath}`);
